        <section>
            <h2 class="project-header">Context &amp; Challenge</h2>
            <p>
            For my Web Scripting II final project, I was tasked with creating a dynamic, data-driven recipe website. The goal was to build a fully functional online cookbook that allowed users to browse, search, and filter recipes seamlessly. 

            </p>
            <p><b>This project represented the culmination of everything I learned in the course, from server-side scripting and database management to authentication and secure coding practices.</b></p>
            <p>
            The challenge was multifaceted. I needed to integrate a database to store and retrieve recipe information efficiently while ensuring the site was intuitive and visually appealing.

            <p>
            This meant balancing technical requirements like CRUD operations and prepared statements with user-centric features like filtering by cuisine and an engaging UI. The timeline was tight, with just a few weeks to transform the initial concept into a polished application.
            </p>
            <p>The project goals were clear:</p>
            <ol>
                <li>Develop a branded, responsive recipe website.</li>
                <li>Enable users to easily search, browse, and filter recipes.</li>
                <li>Ensure secure handling of data with validated inputs and database connections.</li>
                <li>Apply best practices in server-side scripting to create a scalable and maintainable application.</li>
            </ol>
            <p>
            This project challenged me to combine creativity, technical skills, and problem-solving to deliver a cohesive solution that both looked great and performed reliably.
            </p>
        </section>

        <section>
            <h2 class="project-header">Process &amp; Insight</h2>
            <p>
            I began this project by analyzing the user experience requirements and breaking the functionality into manageable components. My first step was designing the database schema to store recipe details effectively, including fields for recipe names, cuisine types, ingredients, steps, and images. This allowed me to visualize how data would flow from the database to the user interface.

            </p>
            
            <p>
            From there, I created wireframes to map out the user journey, focusing on simplicity and usability. I prioritized features like recipe search, filtering by cuisine, and a detailed recipe page that included clear instructions and visuals. Using PHP, I connected the database to dynamically display recipes based on user queries and selections.
            </p>
            
            <p>
                Whether I was testing out changes myself through code or showing my progress to peers, I made sure to <b>ask questions, search for solutions online, and make iterative changes</b> based on feedback from others. 
            </p>
        </section>

